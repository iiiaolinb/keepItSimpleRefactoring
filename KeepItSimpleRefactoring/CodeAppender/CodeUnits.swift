enum CodeUnits: String {
    case space = " "
    case enter = "\n"
    case tab = "\t"
    case dot = "."
    case comma = ","
    case equal = "="
    case figureBracketOpen = "("
    case figureBracketClose = ")"
    case squareBracketOpen = "["
    case squareBracketClose = "]"
    case curleBracketOpen = "{"
    case curleBracketClose = "}"
    case doubleSlash = "//"
    case commentString = "//MARK: - ***"
    case privateString = "private"
    case funcString = "func"
    case varString = "var"
    case letString = "let"
}
